"""
**Simple Calculator**: Write a function for each Arithmetic Operation. Call this function with different values.
"""

def Add(num1, num2):
    return num1 + num2

def Sub(num1, num2):
    return num1 - num2

def Multiply(num1, num2):
    return num1 * num2

def Divide(num1, num2):
    return num1 / num2

print(f"Sum of two Numbers >> {Add(17,2)}")
print(f"Substraction of two Numbers >> {Sub(17,2)}")
print(f"Multiplication of two Numbers >> {Multiply(17,2)}")
print(f"Division of two Numbers >> {Divide(17,2)}")
